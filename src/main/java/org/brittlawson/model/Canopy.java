package org.brittlawson.model;

public class Canopy {

    private int canopyId;
    private String platform;
    private int sizeSquareFeet;

    public int getCanopyId() {
        return canopyId;
    }

    public void setCanopyId(int canopyId) {
        this.canopyId = canopyId;
    }

    public String getPlatform() {
        return platform;
    }

    public void setPlatform(String platform) {
        this.platform = platform;
    }

    public int getSizeSquareFeet() {
        return sizeSquareFeet;
    }

    public void setSizeSquareFeet(int sizeSquareFeet) {
        this.sizeSquareFeet = sizeSquareFeet;
    }
}
